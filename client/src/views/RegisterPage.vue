<script>
import { RouterLink } from 'vue-router'
import { useAllStatesStore } from '../stores/allState'
import { mapActions } from 'pinia'
export default {
  name: "RegisterPage",
  data() {
    return {
      inputEmailRegister: '',
      inputPasswordRegister: ''
    }
  },
  components: {
    RouterLink
  },
  methods: {
    ...mapActions(useAllStatesStore, ['register']),
    localRegister() {
      this.register({
        email: this.inputEmailRegister,
        password: this.inputPasswordRegister
      })
    }
  }
}
</script>

<template>
  <section class="vh-100" style="background-color: lightcoral;">
    <div class="container py-5 h-100">
